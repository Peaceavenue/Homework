export class Calculator1 {
    static sum(a: number, b: number) {
        return a + b;
    }
    static sub(a: number, b: number) {
        return a - b;
    }
    static multi(a: number, b: number) {
        return a * b;
    }
    static div(a: number, b: number) {
        return a / b;
    }
    static remain(a: number, b: number) {
        return a % b;
    }
    static exp(a: number, b: number) {
        return a ** b;
    }
}